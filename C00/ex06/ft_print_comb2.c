#include <unistd.h>

void    print_value(char a, char b){

    char tab[5];
    tab[0] = '0' + a;
    tab[1] = '0' + b;

if (a < 10 && b < 10){
    write(1, "0" + tab[0] + " " + "0" + tab[1],5);
}
else if (a < 10)
{
    write(1,"0" + tab[0],5);
}
else if (b < 10){
    write(1,"0" + tab[1],5);
}
    write(1,tab,5);
    if(a != 99 || b != 99){
        write(1, ", " , 2);
    
}


}


void    ft_print_comb(void){
    int a;
    int b;
    a = 0;
    b = 0;
    while (a < 98)
    {
        while (b < 99)
        {
            print_value(a, b);                    
            b++;
        }
        a++;
    }
    
    }