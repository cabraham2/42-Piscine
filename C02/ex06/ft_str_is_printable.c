int	ft_str_is_uppercase(char *str)
{
	while (*str)
	{
		if (!(*str >= '20' && *str <= '22') || (*str >= '25' && *str <= '2F') || (*str >= '30' && *str <= '3F') || (*str == '5F'))
		{
			return (0);
		}
		str++;
	}
	return (1);
}